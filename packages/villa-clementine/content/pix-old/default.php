			<h2>Maison en location de vacances a La Roquette sur Siagne (06)</h2>
	
			<ul>
				<li>Maison de 5 pièces sur 140 m², orientée sud et ouest, avec 3 chambres et piscine privée. Vue dégagée sur la mer et les collines du Tanneron.</li>
				<li>Idéalement située au calme, à 6km de Cannes et de Mandelieu.</li>
				<li>2 chambres avec lit double, salle d'eau privée et toilettes.</li>
				<li>1 suite parentale avec lit double, salle de bains et toilettes privés, et solarium de 24m².</li>
				<li>Salon et salle à manger double exposition, avec grandes baies vitrées offrant une belle vue dégagée sur les collines et la baie de Mandelieu.</li>
				<li>Cuisine séparée avec grand réfrigérateur, lave-vaisselle, lave-linge, sèche-linge, four, plaque gaz et cellier.</li>
