function getOrderedListByCat() {
  const orderedImages = {}
  Object.keys(images).forEach(cat => {
    Object.keys(images[cat]).forEach(img => {
      if (!(cat in orderedImages)) {
        orderedImages[cat] = []
      }
      orderedImages[cat].push(`${img}.jpg`)
    })
  })
  return orderedImages
}

